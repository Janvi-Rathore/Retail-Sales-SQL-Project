-- View 1:
-- Create a Customer Sales Summary View.

create view vw_customer_sales as

select
    c.customer_key,
    concat(c.first_name,' ',c.last_name) as customer_name,
    sum(f.sales) as total_sales,
    count(f.sale_id) as total_orders
from fact_sales f
join dim_customer c
on f.customer_key = c.customer_key
group by
    c.customer_key,
    customer_name;

-- Verify Customer Sales View

select * from vw_customer_sales;


-- View 2:
-- Create a Product Sales Summary View.

create view vw_product_sales as

select
    p.product_key,
    p.product_name,
    p.category,
    sum(f.quantity) as total_quantity,
    sum(f.sales) as total_sales
from fact_sales f
join dim_product p
on f.product_key = p.product_key
group by
    p.product_key,
    p.product_name,
    p.category;
 
-- Verify Product Sales View
 
select * from vw_product_sales;


