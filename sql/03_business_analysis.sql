Retail Sales SQL Project
Phase 3 : Business Analysis

Description:
This script performs business analysis on the retail sales
database using SQL queries. It answers key business
questions related to sales performance, customers,
products, stores, employees, and sales trends to
generate meaningful business insights.
=========================================================
*/

use retail;

show tables;


-- =====================================================
-- Sales KPI Analysis
-- Calculate key performance indicators (KPIs) to
-- evaluate overall business performance.
-- =====================================================

-- Business Question 1:
-- What is the total sales generated?

select sum(sales) as total_sales
from fact_sales;

-- Business Question 2:
-- How many total orders were placed?

select count(sale_id) as total_orders
from fact_sales;

-- Business Question 3:
-- How many unique customers made purchases?

select count(distinct customer_key) as total_customers
from fact_sales;

-- Business Question 4:
-- What is the average order value?

select round(avg(sales),2) as average_order_value
from fact_sales;


-- =====================================================
-- Customer Analysis
-- Analyze customer purchasing behavior and identify
-- valuable as well as inactive customers.
-- =====================================================

-- Business Question 5:
-- Identify the Top 10 Customers by Total Sales.

select
    c.customer_key,
    concat(c.first_name,' ',c.last_name) as customer_name,
    sum(f.sales) as total_sales
from fact_sales f
join dim_customer c
on f.customer_key = c.customer_key
group by
    c.customer_key,
    customer_name
order by total_sales desc
limit 10;

-- Business Question 6:
-- Find Customers Who Have Never Placed an Order.

select
    c.customer_key,
    concat(c.first_name,' ',c.last_name) as customer_name
from dim_customer c
left join fact_sales f
on c.customer_key = f.customer_key
where f.customer_key is null;

-- Business Question 7:
-- Identify Repeat Customers Based on Number of Orders.

select
    c.customer_key,
    concat(c.first_name,' ',c.last_name) as customer_name,
    count(f.sale_id) as total_orders
from fact_sales f
join dim_customer c
on f.customer_key = c.customer_key
group by
    c.customer_key,
    customer_name
having count(f.sale_id) > 1
order by total_orders desc;


-- =====================================================
-- Product Analysis
-- Evaluate product performance by analyzing sales,
-- demand, and category-wise revenue.
-- =====================================================

-- Business Question 8:
-- Which are the Top 10 best-selling products based on quantity sold?

select
    p.product_name,
    sum(f.quantity) as total_quantity
from fact_sales f
join dim_product p
on f.product_key = p.product_key
group by p.product_name
order by total_quantity desc
limit 10;

-- Business Question 9:
-- Which are the Bottom 10 products based on quantity sold?

select
    p.product_name,
    sum(f.quantity) as total_quantity
from fact_sales f
join dim_product p
on f.product_key = p.product_key
group by p.product_name
order by total_quantity
limit 10;

-- Business Question 10:
-- Which products have never been sold?

select
    p.product_key,
    p.product_name
from dim_product p
left join fact_sales f
on p.product_key = f.product_key
where f.product_key is null;

-- Business Question 11:
-- Which product categories generate the highest revenue?

select
    p.category,
    sum(f.sales) as total_sales
from fact_sales f
join dim_product p
on f.product_key = p.product_key
group by p.category
order by total_sales desc;


