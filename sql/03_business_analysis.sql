-- =====================================================
-- Date & Time Analysis
-- Analyze sales trends over different time periods
-- including daily, monthly, quarterly, and yearly performance.
-- =====================================================

-- Business Question 20:
-- What are the daily sales trends?

select
    d.full_date,
    sum(f.sales) as total_sales
from fact_sales f
join dim_date d
on f.date_key = d.date_key
group by d.full_date
order by d.full_date;

-- Business Question 21:
-- What are the monthly sales trends?

select
    year(d.full_date) as year,
    month(d.full_date) as month,
    sum(f.sales) as total_sales
from fact_sales f
join dim_date d
on f.date_key = d.date_key
group by
    year(d.full_date),
    month(d.full_date)
order by
    year,
    month;

-- Business Question 22:
-- What are the quarterly sales trends?

select
    year(d.full_date) as year,
    quarter(d.full_date) as quarter,
    sum(f.sales) as total_sales
from fact_sales f
join dim_date d
on f.date_key = d.date_key
group by
    year(d.full_date),
    quarter(d.full_date)
order by
    year,
    quarter;

-- Business Question 23:
-- What are the yearly sales trends?

select
    year(d.full_date) as year,
    sum(f.sales) as total_sales
from fact_sales f
join dim_date d
on f.date_key = d.date_key
group by year(d.full_date)
order by year;


